// Aspect ratios
const ICON_RATIO = 1          // 1080 / 1080
const FULL_RATIO = 1080 / 283.66  // ≈ 3.808
const WORD_RATIO = 1080 / 283.66  // same viewBox as logotipo

export function Logo({
  variant = 'full',
  size = 32,
  color = 'currentColor',
  accentColor = '#925cf5',
  className,
  style,
  'aria-label': ariaLabel = 'New Haze',
}: LogoProps) {
  if (variant === 'icon') {
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 1080 1080"
        width={size * ICON_RATIO}
        height={size}
        className={className}
        style={style}
        aria-label={ariaLabel}
        role="img"
      >
        <Isotype color={color} accentColor={accentColor} />
      </svg>
    )
  }

  if (variant === 'wordmark') {
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 1080 283.66"
        width={size * WORD_RATIO}
        height={size}
        className={className}
        style={style}
        aria-label={ariaLabel}
        role="img"
      >
        <Wordmark color={color} />
      </svg>
    )
  }

  // variant === 'full' — isotype + wordmark composed side by side
  // The imagotype SVG has both in a single 1080×283.66 canvas
  const iconScale = 283.66 / 1080  // isotype scaled to fit the imagotype height
  const iconWidth = 1080 * iconScale  // ≈ 283.66px (square isotype scaled to canvas height)
  const gap = 36                      // gap between icon and wordmark in viewBox units
  const wordStart = iconWidth + gap
  const totalWidth = wordStart + 1080

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox={`0 0 ${totalWidth} 283.66`}
      width={size * (totalWidth / 283.66)}
      height={size}
      className={className}
      style={style}
      aria-label={ariaLabel}
      role="img"
    >
      {/* Isotype scaled to fit 283.66×283.66 */}
      <g transform={`scale(${iconScale})`}>
        <Isotype color={color} accentColor={accentColor} />
      </g>
      {/* Wordmark shifted right of the isotype */}
      <g transform={`translate(${wordStart}, 0)`}>
        <Wordmark color={color} />
      </g>
    </svg>
  )
}
